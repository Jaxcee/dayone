package com.bridgelabz.practicedayone;

public class swap {
    public static void main(String[] args) {
        int fn=5;
        int sn=6;

        System.out.println(fn);
        System.out.println(sn);
        int temp=fn;
        fn=sn;
        sn=temp;
        System.out.println(fn);
        System.out.println(sn);



    }
}
